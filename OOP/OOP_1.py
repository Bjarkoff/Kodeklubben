# Betragt nedenstående kode:
import turtle

peter = turtle.Turtle()
peter.speed(1)
peter.goto(100,100)
peter.left(90)
peter.forward(100)
peter.left(90)
peter.forward(200)

# Det som sker her, at vi skaber en turtle ved navn peter. 
# En skildpadde kommer med en hel masse spændende attributter.
# Som I har set, kan den bevæge sig frem og tilbage, dreje et antal grader,
# tegne i farver, ændre sin fart med mere.
